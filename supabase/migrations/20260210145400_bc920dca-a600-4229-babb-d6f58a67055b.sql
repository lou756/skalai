INSERT INTO storage.buckets (id, name, public) VALUES ('sitemaps', 'sitemaps', true);

CREATE POLICY "Public can read sitemaps"
ON storage.objects FOR SELECT
USING (bucket_id = 'sitemaps');

CREATE POLICY "Service role can upload sitemaps"
ON storage.objects FOR INSERT
WITH CHECK (bucket_id = 'sitemaps');
